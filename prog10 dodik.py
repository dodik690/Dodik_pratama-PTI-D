# >,  <, >=,  <=, ==,  !=, is, isnot

x = 5

hasil = x > 6
print("x > 6 = ", hasil)

hasil = x < 6
print("x < 6 = ", hasil)

hasil = x >= 6
print("x >= 6 = ", hasil)

hasil = x <= 6
print("x < 6= = ", hasil)

hasil = x == 6
print("x == 6 = ", hasil)

hasil = x != 6
print("x != 6 = ", hasil)